RHO_O = 805 # density of organic phase kg/m3
RHO_W = 996 # density of water phase kg/m3
ETA_O = 0.614e-3 # viscosity of organic phase Pa*s
ETA_W = 1.066e-3 # viscosity of water phase Pa*s
DELTA_RHO = abs(RHO_W-RHO_O) # density difference in kg/m3
R_IG = 8.314 # ideal gas constant J/mol*K
RHO_G = 1.2 # density of gas kg/m3
M_G = 28.97e-3 # molar mass of gas kg/mol
SIGMA = 0.0102 # surface tension N/m
R_V = 0.0384 # asymetric film drainage parameter